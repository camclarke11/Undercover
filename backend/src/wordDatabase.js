// Get all unique categories
function getCategories() {
  return [...new Set(wordPairs.map(p => p.cat))];
}

// Get random word pair (optionally filtered by category)
function getRandomWordPair(category = null) {
  let pairs = wordPairs;
  if (category) {
    pairs = wordPairs.filter(p => p.cat === category);
  }
  if (pairs.length === 0) {
    pairs = wordPairs; // Fallback to all if category empty
  }
  const index = Math.floor(Math.random() * pairs.length);
  return pairs[index];
}

// Get word pairs by category
function getWordPairsByCategory(category) {
  return wordPairs.filter(p => p.cat === category);
}

// Get count of pairs per category
function getCategoryCounts() {
  const counts = {};
  wordPairs.forEach(p => {
    counts[p.cat] = (counts[p.cat] || 0) + 1;
  });
  return counts;
}

// Get total number of word pairs
function getTotalPairs() {
  return wordPairs.length;
}

